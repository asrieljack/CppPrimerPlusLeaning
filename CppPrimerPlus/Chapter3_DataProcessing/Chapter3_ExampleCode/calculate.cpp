// Program File 3.4  Calculate
#include <iostream>
using namespace std;
int main() {
  int a = 10;
  int b = 3;
  int c = 5;
  int d = 4;
  cout << "a+b: " << a + b << endl;
  cout << "a-b: " << a - b << endl;
  cout << "a*b: " << a * b << endl;
  cout << "a/b: " << a / b << endl;
  cout << "a%b: " << a % b << endl;
  cout << "a+b*c: " << a + b * c << endl;
  cout << "a*b+c*d: " << a * b + c * d << endl;
  cout << "a*(b+c)*d: " << a * (b + c) * d << endl;
}